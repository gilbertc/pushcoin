{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

- (void)messageDidUpdatedBy:(id<MessageUpdaterDelegate>)updater
{
    self.refreshControl.attributedTitle = [[NSAttributedString alloc] initWithString:@"Pull to refresh"];
    [self.refreshControl endRefreshing];
    
    [self.tableView reloadData];
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    NSMutableArray * array = self.appDelegate.mainTabBarController.transactions;
    if (array != nil)
        return array.count;
    return 0;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    id array = self.appDelegate.mainTabBarController.transactions;
    if (array == nil)
        return nil;
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    if (cell == nil)
    {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:CellIdentifier];
    }
    
    cell.textLabel.text = ((Transaction *) [array objectAtIndex:indexPath.row]).counterPartyName;
    cell.detailTextLabel.text = ((Transaction *) [array objectAtIndex:indexPath.row]).payment.text;
    
    return cell;
}

